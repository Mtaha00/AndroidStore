package com.example.androidstore.model;

public class Brand {

    private String id;
    private String name;
    private String ling_img;

    public Brand() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLing_img() {
        return ling_img;
    }

    public void setLing_img(String ling_img) {
        this.ling_img = ling_img;
    }
}
